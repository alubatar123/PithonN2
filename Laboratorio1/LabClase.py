import math

class Trig():

    def __init__(self):
        self.valor_pi=math.pi 
    def Calcu_Seno(self):
        return "El seno de pi es " + str (math.sin(self.valor_pi))    
    def Calcu_Coseno(self):
        return "El coseno de pi es " + str (math.cos(self.valor_pi))    
    def Calcu_Tangente(self):        
        return "La tangente de pi es " + str (math.tan(self.valor_pi))    


 




        
    